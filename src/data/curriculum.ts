// Helper functions remain the same
export const getSubjectsForClass = (classNumber: number) => {
  if (classNumber >= 11) {
    return ['English', 'Physics', 'Chemistry', 'Mathematics', 'Biology', 'Economics', 'Accountancy', 'Business Studies'];
  }
  if (classNumber >= 6 && classNumber <= 10) {
    return ['English', 'Hindi', 'Mathematics', 'Science', 'Social Science'];
  }
  return [];
};

export const getChaptersForSubject = (subject: string, classNumber: number) => {
  const key = `${subject}-${classNumber}`;
  return chapters[key as keyof typeof chapters] || [];
};